class TestS01Observability:
    """S01 regression verifies audit fields exist on responses."""

    def test_decision_trace_present_for_place_response(self, client):
        """Place recommendation responses must include decision_trace."""
        candidates = [
            _make_candidate("places/trace", "Quán Trace", local_factor=0.9, rating=4.5),
        ]
        service = _mock_recommend_service(candidates=candidates)
        _install_recommend_service(client, service)

        r = client.post("/chat", json={
            "session_id": "s-s01-decision-trace",
            "message": "nhà hàng",
            "language": "vi",
        })

        body = r.json()
        assert body["decision_trace"] is not None
        trace = body["decision_trace"]
        assert "events" in trace
        assert "credential_status" in trace
        assert "provider_source" in trace
        assert "session_id" in trace

    def test_fairness_audit_present_for_place_response(self, client):
        """Place recommendation responses must include fairness_audit."""
        candidates = [
            _make_candidate("places/fair", "Quán Fair", local_factor=0.9),
        ]
        service = _mock_recommend_service(candidates=candidates)
        _install_recommend_service(client, service)

        r = client.post("/chat", json={
            "session_id": "s-s01-fairness",
            "message": "nhà hàng",
            "language": "vi",
        })

        body = r.json()
        assert body["fairness_audit"] is not None
        audit = body["fairness_audit"]
        assert "candidate_count" in audit
        assert "result_count" in audit
        assert "top5_local_ratio" in audit
        assert "provider_status" in audit
        assert "warnings" in audit

    def test_audit_fields_no_secret_exposure(self, client):
        """Audit fields (decision_trace, fairness_audit, reasoning_log) must not leak secrets."""
        candidates = [_make_candidate("places/audit", "Quán Audit", local_factor=0.8)]
        service = _mock_recommend_service(candidates=candidates)
        _install_recommend_service(client, service)

        r = client.post("/chat", json={
            "session_id": "s-s01-audit-redact",
            "message": "nhà hàng",
            "language": "vi",
        })

        body = r.json()
        raw = json.dumps(body).lower()

        for secret in ("fake-test-key", "api_key", "secret", "password", "token"):
            assert secret not in raw, f"Audit fields must not contain '{secret}'"
