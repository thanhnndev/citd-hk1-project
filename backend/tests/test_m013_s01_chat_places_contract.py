def _named_places_in_message(body: dict) -> set[str]:
    """Extract display_name values that appear in the response message.

    Looks for the numbered list pattern 'N. Name' generated by _message_for_status.
    """
    msg = body.get("message", "")
    # The pattern is: "1. Name A; 2. Name B; 3. Name C"
    # Also matches: "Name A; Name B" without numbers
    import re
    # Pattern: number-dot-space then name up to semicolon/period/end
    found = set()
    # Match "1. Some Name" patterns
    for m in re.finditer(r'\d+\.\s+([^;]+?)(?:;|$)', msg):
        found.add(m.group(1).strip())
    # Match standalone name references after colon
    for m in re.finditer(r'[^;]*?([^;,]{3,})(?:;|$)', msg):
        pass  # handled above
    return found


